# crie um script que leia duas notas de um aluno, calcule e mostre a sua media

nota1 = int(input('informe a nota 1: '))
nota2 = int(input('informe a nota 2: '))

print('A média entre {} e {} é: {}'.format(nota1, nota2, (nota1 + nota2) / 2))
