# crie um script que leia o salario de um funcionario e mostre seu novo salario com 15% de aumento
salario = float(input('informe o salario: '))

print('O salario é de: {}'.format(salario))
print('O aumento foi de: {}'.format(salario * 0.15))
print('O novo salario é de: {}'.format(salario + (salario * 0.15)))
