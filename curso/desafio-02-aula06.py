# faça um programa que leia algo pelo teclado e mostre
# na tela o seu tipo primitivo e todas as informações possíveis sobre ele

n1 = input('insira uma informação: ')

print('isalnum: {}'.format(n1.isalnum()))
print('isalpha: {}'.format(n1.isalpha()))
print('isdecimal: {}'.format(n1.isdecimal()))
print('isdigit: {}'.format(n1.isdigit()))
print('isidentifier: {}'.format(n1.isidentifier()))
print('islower: {}'.format(n1.islower()))
print('isnumeric: {}'.format(n1.isnumeric()))
print('isprintable: {}'.format(n1.isprintable()))
print('isspace: {}'.format(n1.isspace()))
print('istitle: {}'.format(n1.istitle()))
print('isupper: {}'.format(n1.isupper()))
